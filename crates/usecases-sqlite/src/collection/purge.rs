use super::*;

pub fn purge(connection: &SqliteConnection, uid: &EntityUid) -> Result<()> {
    let repo = RepoConnection::new(connection);
    let id = repo.resolve_collection_id(uid)?;
    repo.purge_collection_entity(id).map_err(Into::into)
}
